/**
 * Возвращает данные праздника для даты (ключ MM-DD) или null.
 */
export function getHolidayForDate(date: Date): { emoji: string; caption: string } | null {
  const month = String(date.getMonth() + 1).padStart(2, '0');
  const day = String(date.getDate()).padStart(2, '0');
  const key = `${month}-${day}`;
  return holidays[key] ?? null;
}
