/**
 * Типы для квартального планирования v2.
 * Таймлайн: эпики → стори, одна фаза (разработка) на стори, разбивка по спринтам и дням (одна ячейка = один день).
 */

/** Позиция фазы разработки стори в таймлайне (индекс спринта в квартале, день в спринте, длительность в днях) */
export interface StoryPhasePosition {
  /** Длительность в рабочих днях */
  durationDays: number;
  /** Индекс спринта в списке спринтов квартала (0-based) */
  sprintIndex: number;
  /** День внутри спринта (0-based, рабочие дни) */
  startDay: number;
}
